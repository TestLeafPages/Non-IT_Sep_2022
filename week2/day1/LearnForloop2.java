package learnjava;

public class LearnForloop2 {

	public static void main(String[] args) {
		
		int noOfStudents=45;
		
		for(int i=15;i<=45;i++) {
			System.out.println(i);
			
			//i=15
			//i=i+1-->16
			
		}
		
		
		
		for(int i=45;i>=15;i--) {
			//45>=15
			System.out.println(i);
			//i=1-1-->44
			
		}
		
		
		

	}

}
